#declarar la función
def restar(num1,num2):
    return num1-num2

#invocar la función
resta=restar(5,5)
print(resta)

#declarar la función lambda λ
multiplicar_numeros = lambda num1,num2:num1*num2

#invocar la función λ
multiplicar=multiplicar_numeros(5,5)
print(multiplicar)

